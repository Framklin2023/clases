package clales.dos.tres;

public class Cuatro {
    public static int edo=0;
    private float n1;
    private float n2;
    public float msum(float n1, float n2){
        return n1+n2;
    }
    public float mres(float n1, float n2){
        return n1-n2;
    }
    public static int inc(){
        edo++; 
        return edo;
    }
    
}
